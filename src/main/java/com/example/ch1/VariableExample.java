package com.example.ch1;

public class VariableExample {
    public static void main(String[] args) {
        // 변수 선언
        int age;
        // 변수 초기화
        age = 24;
        // 선언과 동시에 초기화
        double value = 40.8;
        System.out.println("Age: " + age);
        System.out.println("Value: " + value);
    }
}
